	/**
	 * 生命周期函数--监听页面显示
	 */
	onShow: function () {
		var that = this;
		// 查询用户数据
		util.ajax({
			url: app.globalData.path + 'User/user',
			method: 'GET',
			data: {
				userId: wx.getStorageSync('userData').id,
			},
			success: function (res) {
				//成功
				if (res.data.code == 0) {
					wx.setStorageSync('userData', res.data.data);
					that.setData({
						userInfo: res.data.data
					});
				}
			}
		})
	},

